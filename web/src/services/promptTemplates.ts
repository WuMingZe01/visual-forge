export function loadAiPrompts(): AiPromptSet {
  try {
    const raw = localStorage.getItem(LS_KEY);
    if (!raw) return { ...DEFAULT_AI_PROMPTS };
    const saved = JSON.parse(raw) as Partial<AiPromptSet>;
    return { ...DEFAULT_AI_PROMPTS, ...saved };
  } catch {
    return { ...DEFAULT_AI_PROMPTS };
  }
}

export function saveAiPrompts(prompts: AiPromptSet): void {
  localStorage.setItem(LS_KEY, JSON.stringify(prompts));
}

export function getAiPrompt(key: keyof AiPromptSet): string {
  return loadAiPrompts()[key] || DEFAULT_AI_PROMPTS[key];
}

export function resetAiPrompts(): AiPromptSet {
  saveAiPrompts(DEFAULT_AI_PROMPTS);
  return { ...DEFAULT_AI_PROMPTS };
}
